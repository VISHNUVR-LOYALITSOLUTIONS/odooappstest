from odoo import fields, models, api


class AccountReportPayment(models.TransientModel):
    _name = "cab.booking.report.wizard"
    _description = "Cab Booking Report Wizard"

    company_id = fields.Many2one('res.company', string='Company', readonly=True, default=lambda self: self.env.user.company_id)
    date_from = fields.Date(string='Date From', required=True)
    date_to = fields.Date(string='Date To', required=True)
    driver_id = fields.Many2one('res.users', string="Driver")
    car_id = fields.Many2one('fleet.vehicle', string="Cab")


    def _build_contexts(self, data):
        result = {}
        result['date_from'] = data['form']['date_from'] or False
        result['date_to'] = data['form']['date_to'] or False
        result['driver_id'] = data['form']['driver_id'] or False
        result['car_id'] = data['form']['car_id'] or False
        return result

    def check_report(self):
        self.ensure_one()
        data = {}
        data['ids'] = self.env.context.get('active_ids', [])
        data['model'] = self.env.context.get('active_model', 'ir.ui.menu')
        data['form'] = self.read(['date_from', 'date_to', 'driver_id', 'car_id', 'company_id'])[0]
        used_context = self._build_contexts(data)
        data['form']['used_context'] = dict(used_context, lang=self.env.context.get('lang', 'en_US'))
        return self._print_report(data)

    def _print_report(self, data):
        return self.env.ref('cab_booking.action_report_cab_booking').report_action(self, data=data)









